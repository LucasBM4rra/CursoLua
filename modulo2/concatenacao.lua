--  concatenação é a união de duas strings

local nome = "Lucas"
local emocao = "Feliz"

print("o meu nome eh "..nome.. " e eu estou "..emocao)
print('eu sou ' .. 'feliz')
print(nome .. emocao)

print('sua nota foi ' .. 10)

-- tentativa de contatenar uma string com nill
print('sua nota foi ' .. nil)
